package loans;

public class Month {
    private int month;
    private double monthlyPay;
    private double monthlyPrincipal;
    private double monthlyInterest;
    private double balanceOwed;


    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public double getMonthlyPay() {
        return monthlyPay;
    }

    public void setMonthlyPay(double monthlyPay) {
        this.monthlyPay = monthlyPay;
    }

    public double getMonthlyPrincipal() {
        return monthlyPrincipal;
    }

    public void setMonthlyPrincipal(double monthlyPrincipal) {
        this.monthlyPrincipal = monthlyPrincipal;
    }

    public double getMonthlyInterest() {
        return monthlyInterest;
    }

    public void setMonthlyInterest(double monthlyInterest) {
        this.monthlyInterest = monthlyInterest;
    }

    public double getBalanceOwed() {
        return balanceOwed;
    }

    public void setBalanceOwed(double balanceOwed) {
        this.balanceOwed = balanceOwed;
    }



}
